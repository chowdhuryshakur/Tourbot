package tourbot;

public class chatRecord {
    
   private String user;
   private String pijon;

    public chatRecord(String user, String pijon) {
        this.user = user;
        this.pijon = pijon;
    }

    public String getUser() {
        return user;
    }

    public String getPijon() {
        return pijon;
    }

    
    
    
}
